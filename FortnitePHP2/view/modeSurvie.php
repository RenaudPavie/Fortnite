    	<h2 id="presentation">Présentation du mode de jeu</h2>

    	<p>Le mode " Sauver le monde " de fortnite est un mode PVE ( Player Versus Environnement ) où le joueurs effectuent des missions, certaines dans un temps imparti, certaines illimitées au niveau du temps. <br>
    	</p>

    	<h2 id="modes">Présentation de la carte et des modes de jeu</h2>
    	<a href="img/fontainebois.jpg"><img id="fontainebois" title="Carte de fontainebois" src="img/fontainebois_mini.jpg" alt="carte de fontainebois fortnite"></a>

    	<p>On peut voir sur cette carte différents modes et différentes couleurs, commençons par les couleurs, c'est un indiquateur représentant le niveau de difficulté.</p>
    	
