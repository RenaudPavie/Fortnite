			<h2 id="loot">Les loots et les fonctionnalitées</h2>

			<h3>Le Air Drop ou Supply drop</h3>

			<img id="imgAirdrop" Title="Airdrop dans Battle Royale" src="img/Airdrop.jpg" alt="Airdrop"><br>

			<p>Le Air drop est parachuté aléatoirement sur la carte à certains moments de la partie, c'est une caisse de ravitaillement qui peut contenir diverses choses : </p>
			
			<ul class="quality" id="airdrop" >
				<li style="color:orange">Des armes légendaires</li>
				<li style="color:blue">Des potions de boucliers</li>
				<li style="color:red">Des kits de soins</li>
				<li style="color:red">Des bandages</li>
			</ul>

			<p>A savoir que ces zones de ravitaillement font souvent l'objet de zones de guerres intenses, si votre objectif est de survivre, ne vous focalisez pas sur ces airdrops à moins qu'ils n'attérissent à cotés de vous. En revanche, si vous recherchez les kills et des zones de combats pour un gameplay plus dynamique, ce sont souvent des zones qui vous conviendront parfaitement !</p>


			<h2 id="survivre">Comment survivre ?</h2>

			<p>Tout d'abord, le parachutage. Il faut choisir l'endroit où l'ont souhaite atterir pour pouvoir être tranquille sans que 20 joueurs ne vous saute dessus à peine tombé ! Choisir des extrémitées de villes, ou des maisons isolées est une bonne solution.</p>

			<p>Une foi attérit, il faut maintenant vous équipez ! Le mieux est d'attérir sur des toits de batiments et de casser celui-ci, souvent des coffres s'y trouvent et c'est le meilleur start game possible, si vous atterissez en premier, vous aurez donc l'avantage en ayant une arme tandis que les autres joueurs seront encore nu.</p>
